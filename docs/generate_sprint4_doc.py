H1(doc, "5. Technical Challenges & Resolutions")

H3(doc, "Challenge 1 — Audit Hook Without Double-Counting")
body(doc,
    "Issue: A naive audit hook fires on every approval, even when the user "
    "didn’t change anything — flooding the log with no-op rows.")
body(doc,
    "Resolution: The hook compares (old_value, new_value) and writes a row "
    "only when they differ. The correction_count column is incremented only "
    "when the user overrides the extractor’s output (not on first approval), "
    "which keeps KPI 2 honest.")

H3(doc, "Challenge 2 — KPI Baseline Without Real Production Data")
body(doc,
    "Issue: The proposal’s KPI targets are stated as percentage improvements "
    "against a manual-workflow baseline that doesn’t exist for our team.")
body(doc,
    "Resolution: We adopted baseline values derived from the proposal’s "
    "cited references (Roland Berger RPA report; Kumar & Singh 2025; "
    "Nutrient IDP). These constants live in main.py as named constants "
    "(BASELINE_MINUTES_PER_CASE, etc.), making the choice transparent and "
    "easy to revise once a real baseline study is run in Sprint 5.")

H3(doc, "Challenge 3 — PDF Generation in Memory Under Streaming")
body(doc,
    "Issue: Returning a PDF from reportlab through FastAPI without temp "
    "files required threading a BytesIO buffer through the response layer.")
body(doc,
    "Resolution: exporter.py builds into a BytesIO buffer; main.py wraps "
    "the buffer in a StreamingResponse with the correct MIME type and a "
    "Content-Disposition header. The browser triggers a native download "
    "with no flicker.")

H3(doc, "Challenge 4 — Isolated Tests Without Touching the Production DB")
body(doc,
    "Issue: The original code wires SQLAlchemy directly to "
    "logistics_automation.db, so naive tests would corrupt local dev data.")
body(doc,
    "Resolution: A pytest fixture overrides FastAPI’s get_db dependency "
    "with an in-memory SQLite engine using StaticPool. Each test runs in a "
    "fresh empty database, and the suite completes in ~5.7 seconds.")

