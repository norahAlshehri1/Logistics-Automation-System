def bullet(doc, text, level=0):
    p = doc.add_paragraph(style="List Bullet")
    p.paragraph_format.left_indent = Cm(0.5 + level * 0.7)
    run = p.runs[0] if p.runs else p.add_run("")
    run.text = ""
    run = p.add_run(text)
    run.font.size = Pt(11)
    run.font.color.rgb = TEXT_DARK
    return p


